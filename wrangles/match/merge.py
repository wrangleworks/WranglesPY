def cleanPart(value):
    value = re.sub(r"^\W+", "", value)
    value = re.sub(r"\W+$", "", value)
    return value


def pick_best_part(potentials):
    """
    Attempts to pick the best part from a list
    Used if there is no better information to go off
    """
    # Sort by length descending
    potentials = sorted(list(potentials), key=len, reverse=True)

    # Loop through
    # if the part contains two tokens of 5 or more chars, discard as this is probably two concatenated together
    # Otherwise return the first, i.e. longest, result
    for potential in potentials:
        tokens = potential.split()
        tokens_long_count = 0
        for token in tokens:
            if len(token) >= 5:
                tokens_long_count += 1
        
        if tokens_long_count < 2:
            return potential
                

def count_space(string):
    # counter
    count = 0
    # loop for search each index
    for i in string:
        # Check each char
        # is blank or not
        if i == " ":
            count += 1
         
    return count


def convertPartcodeToPattern(partcode):
    pattern = str(partcode).upper()
    pattern = re.sub('[^A-Z0-9]', '', pattern)
    pattern = re.sub('[A-Z]', 'A', pattern)
    pattern = re.sub('[0-9]', '#', pattern)
    return pattern

def convertPartcodeToPatternAlpha(partcode):
    pattern = str(partcode).upper()
    pattern = re.sub('[^A-Z0-9]', '', pattern)
    pattern = re.sub('[0-9]', '#', pattern)
    return pattern


